package io.aklivity.zilla.runtime.engine.internal.metrics;

import io.aklivity.zilla.runtime.engine.EngineContext;
import io.aklivity.zilla.runtime.engine.metrics.Metric;
import io.aklivity.zilla.runtime.engine.metrics.MetricContext;

public class EngineWorkerCountMetric implements Metric
{
    private static final String GROUP = EngineMetricGroup.NAME;
    private static final String NAME = String.format("%s.%s", GROUP, "worker.count");
    private static final String DESCRIPTION = "Engine worker count";

    @Override
    public String name()
    {
        return NAME;
    }

    @Override
    public Kind kind()
    {
        return Kind.COUNTER;
    }

    @Override
    public Unit unit()
    {
        return Unit.COUNT;
    }

    @Override
    public String description()
    {
        return DESCRIPTION;
    }

    @Override
    public MetricContext supply(
        EngineContext context)
    {
        //Unsupported metric context
        return null;
    }
}
